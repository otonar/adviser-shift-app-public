-- =====================
-- セッション無効化用のトークンバージョン
-- =====================
-- JWT はステートレスなので、パスワードのリセット/変更をしても既発行トークンは
-- 有効期限まで生き続ける。users 行に token_version を持たせてトークンに埋め込み、
-- 認証時に「トークンの版 == DB の現在の版」を照合する。
-- パスワードのリセット/変更や「全端末ログアウト」で token_version を +1 すれば、
-- 過去に発行したトークンは一斉に無効化される。
ALTER TABLE users
  ADD COLUMN token_version INT NOT NULL DEFAULT 0;
